package chapters.ch07;

public class Operators {
    public static void main(String[] args) {
        int n = 10;
        System.out.println(n);
        System.out.println(n++); // first increase test
        System.out.println(n);
        System.out.println(n--); // first decrease test
        System.out.println(n);
        System.out.println(++n); // second increase test
        System.out.println(n);
        System.out.println(--n); // second decrease test
        System.out.println(n);
    }
}
